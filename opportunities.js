function newCard(Listing) {
  let card = document.createElement('div');

  card.setAttribute("class","Listing")
  card.innerHTML = `
    <img class='l-image' src="${Listing.image || ""}">
    <div class="Listing-Content"> 
      <h3> ${Listing.name} </h3>
      <p> ${Listing.description} </p>
    </div>
  `;
  
  return card;
}

console.log("running");

function getListingsByBorough(Borough) {
  
  let boroughData = boroughOpportunities[Borough]; 
  let uploadedData = localStorage.getItem(Borough.toLowerCase());
  
  let container = document.getElementById("listings-containers");
  container.innerHTML = "";

  if (uploadedData) {
    uploadedData.forEach(bData => {
      container.appendChild(newCard(bData));
    })
  }
  
  boroughData.forEach(bData => {
    container.appendChild(newCard(bData));
  });
}

